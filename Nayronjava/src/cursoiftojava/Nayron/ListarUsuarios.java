    private void preenchercomUsusario() throws ClassNotFoundException, SQLException {
        JogadorDao jdao = new JogadorDao();

        Jogadores = jdao.listaUsuarios();

        String vetor[] = new String[Jogadores.size()];
        int i = 0;

        for (Jogador j : Jogadores) {
            vetor[i] = j.getNome();
            i++;

        }
        jComboBox1.setModel(new javax.swing.DefaultComboBoxModel(vetor));

    }

    public void preencheTabela() throws SQLException, ClassNotFoundException {

        JogadorDao jdao = new JogadorDao();

        Jogadores = jdao.listaUsuarios();

        String array[][] = new String[Jogadores.size()][4];

        int i = 0;
        for (Jogador jogadores : Jogadores) {
            array[i][0] = jogadores.getId() + "";
            array[i][1] = jogadores.getNome() + "";
            array[i][2] = jogadores.getLogin() + "";
            array[i][3] = jogadores.getEmail() + "";
            i++;
        }

        jTable1.setModel(new javax.swing.table.DefaultTableModel(array, new String[]{"C√≥digo", "Nome", "Login", "E-Mail do Usu√°rio"}));


    }
